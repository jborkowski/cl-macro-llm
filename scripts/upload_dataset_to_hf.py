#!/usr/bin/env -S uv run --script
#
# /// script
# requires-python = ">=3.12"
# dependencies = ["huggingface_hub>=0.24"]
# ///
"""Push the locally-enriched (chat + <think> traces) dataset to HuggingFace.

Reads `data/processed/full/train.jsonl` and `data/processed/full/valid.jsonl`
(the messages-format files produced by `scripts/prepare_data_full.py` and
enriched with reasoning traces by `scripts/generate_thinking_traces_full.py`)
and uploads them as `train.jsonl` / `validation.jsonl` to a HuggingFace
dataset repo, alongside a minimal README that declares the splits so
`load_dataset(<repo>)` works out of the box.

Env vars:
  HF_TOKEN          required — token with write access to the target repo
  HF_DATASET_REPO   optional — default `j14i/cl-macros-thinking`
"""

from __future__ import annotations

import json
import os
import sys
import tempfile
from pathlib import Path

from huggingface_hub import HfApi, create_repo


PROJECT_ROOT = Path(__file__).resolve().parents[1]
TRAIN_FILE = PROJECT_ROOT / "data" / "processed" / "full" / "train.jsonl"
VALID_FILE = PROJECT_ROOT / "data" / "processed" / "full" / "valid.jsonl"


README = """---
license: bsd-2-clause
language:
- en
task_categories:
- text-generation
tags:
- common-lisp
- macros
- code-generation
- chain-of-thought
- thinking-traces
configs:
- config_name: default
  data_files:
  - split: train
    path: train.jsonl
  - split: validation
    path: validation.jsonl
---

# cl-macros-thinking

Chat-format Common Lisp macro dataset enriched with `<think>...</think>`
reasoning traces. Each row is a single training example:

```json
{{
  "messages": [
    {{"role": "system",    "content": "You are an expert Common Lisp macro programmer..."}},
    {{"role": "user",      "content": "Write a Common Lisp macro to handle ..."}},
    {{"role": "assistant", "content": "<think>...reasoning...</think>\\n\\n(defmacro ...)"}}
  ]
}}
```

## Splits

| split      | rows |
|------------|------|
| train      | {n_train} |
| validation | {n_valid} |

## Provenance

- Base macro corpus: [`j14i/cl-macros`](https://huggingface.co/datasets/j14i/cl-macros).
- Reformatted to chat format by `scripts/prepare_data_full.py` in
  [cl-macro-llm](https://github.com/jborkowski/cl-macro-llm).
- Reasoning traces generated by `scripts/generate_thinking_traces_full.py`
  using Qwen3.6-27B (MLX 4-bit).

## Use

```python
from datasets import load_dataset
ds = load_dataset("{repo}")
# ds["train"][0]["messages"] -> [system, user, assistant]
```
"""


def count_lines(path: Path) -> int:
    with path.open("rb") as f:
        return sum(1 for _ in f)


def validate_messages_schema(path: Path) -> None:
    """Spot-check the first row so we don't push garbage."""
    with path.open() as f:
        row = json.loads(f.readline())
    if "messages" not in row:
        sys.exit(f"{path} row 1 missing 'messages' field — wrong format.")
    roles = [m["role"] for m in row["messages"]]
    if "assistant" not in roles:
        sys.exit(f"{path} row 1 has no assistant turn: {roles}")


def main() -> int:
    token = os.environ.get("HF_TOKEN")
    repo = os.environ.get("HF_DATASET_REPO", "j14i/cl-macros-thinking")
    if not token:
        sys.exit("HF_TOKEN is not set — cannot upload.")

    for f in (TRAIN_FILE, VALID_FILE):
        if not f.exists():
            sys.exit(f"Missing input: {f}")
        validate_messages_schema(f)

    n_train = count_lines(TRAIN_FILE)
    n_valid = count_lines(VALID_FILE)
    print(f"Local rows: {n_train} train / {n_valid} validation")

    print(f"Creating dataset repo: {repo}")
    create_repo(repo, token=token, exist_ok=True, repo_type="dataset", private=False)

    api = HfApi(token=token)

    with tempfile.TemporaryDirectory() as td:
        readme_path = Path(td) / "README.md"
        readme_path.write_text(
            README.format(n_train=n_train, n_valid=n_valid, repo=repo)
        )
        api.upload_file(
            path_or_fileobj=str(readme_path),
            path_in_repo="README.md",
            repo_id=repo,
            repo_type="dataset",
            commit_message="Add dataset card",
        )

    print("Uploading train.jsonl ...")
    api.upload_file(
        path_or_fileobj=str(TRAIN_FILE.resolve()),
        path_in_repo="train.jsonl",
        repo_id=repo,
        repo_type="dataset",
        commit_message="Upload train split",
    )

    print("Uploading validation.jsonl ...")
    api.upload_file(
        path_or_fileobj=str(VALID_FILE.resolve()),
        path_in_repo="validation.jsonl",
        repo_id=repo,
        repo_type="dataset",
        commit_message="Upload validation split",
    )

    print(f"\nDataset pushed to: https://huggingface.co/datasets/{repo}")
    print(f'Use it on the pod with:  export DATASET="{repo}"')
    return 0


if __name__ == "__main__":
    sys.exit(main())
